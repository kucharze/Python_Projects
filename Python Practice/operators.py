#Operators - symbols that are used to perform specific mathmatical and logical operations on variables and values.
# Take one or more values as input and produce a single value as output. Operators are used to perform operations on variables and values. 
# There are several types of operators in Python, including arithmetic, comparison, logical, assignment, and bitwise operators.

#Arithmetic Operators
# +, -, *, /, %, //, **
# Used to perform basic mathematical operations on numbers. 
# The + operator is used for addition, 
# the - operator is used for subtraction, 
# the * operator is used for multiplication, 
# the / operator is used for division, 
# the % operator is used for modulus (remainder), 
# the // operator is used for floor division (returns the largest integer less than or equal to the result), 
# and the ** operator is used for exponentiation (raises a number to a power).

#Assignment Operators
# =, +=, -=, *=, /=, %=, //=, **=, &=, |=, ^=, >>=, <<=

#Comparison Operators (Also know as Relational Operators)
# ==, !=, >, <, >=, <=
#Compare two values and return a boolean value (True or False) based on the comparison.
# Often used in if statements and loops

#Logical Operators
# and, or, not
# Combine multiple boolean expressions and return a boolean value based on the logical operation.
# Often used in if statements and loops

# Not convered in video.
#Bitwise Operators
# &, |, ^, ~, <<, >>
# Bitwise operators are used to perform operations on binary numbers. 
# They operate on the individual bits of the numbers. 
# The & operator is used for bitwise AND, 
# the | operator is used for bitwise OR, 
# the ^ operator is used for bitwise XOR, 
# the ~ operator is used for bitwise NOT, 
# the << operator is used for left shift, 
# and the >> operator is used for right shift.